package pl.projects.bartek.exercises.przekazywanieDoMetod;



public class GitaraElektryczna {

    String rodzaj;
    int iloscKonwenterow;
    boolean uzywanaPrzezGwiazde;



    public String getRodzaj() {
        return rodzaj;
    }

    public void setRodzaj(String rodzaj) {
        this.rodzaj = rodzaj;
    }

    public int getIloscKonwenterow() {
        return iloscKonwenterow;
    }

    public void setIloscKonwenterow(int iloscKonwenterow) {
        this.iloscKonwenterow = iloscKonwenterow;
    }

    public boolean isUzywanaPrzezGwiazde() {
        return uzywanaPrzezGwiazde;
    }

    public void setUzywanaPrzezGwiazde(boolean uzywanaPrzezGwiazde) {
        this.uzywanaPrzezGwiazde = uzywanaPrzezGwiazde;
    }
}
